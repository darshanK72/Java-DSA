import java.lang.Math;

public class j03MathMethods{
    public static void main(String args[]){

        System.out.println(Math.abs(-23));
        System.out.println(Math.pow(10.20,43));
        System.out.println(Math.sqrt(2352));
        System.out.println(Math.round(23.52));
        System.out.println(Math.floor(25.52));
        System.out.println(Math.ceil(25.52));
        System.out.println(Math.min(52,73));
        System.out.println(Math.max(634,632));
        System.out.println(Math.log(Math.E));
        System.out.println(Math.log10(10));
    }
}
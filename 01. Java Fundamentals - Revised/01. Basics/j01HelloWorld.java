/**
 * Topic: Introduction to Java Program Structure
 * 
 * This simple Java program demonstrates the following:
 * 1. The basic structure of a Java program.
 * 2. The use of the `main` method, which serves as the entry point for Java applications.
 * 3. The usage of the `System.out.println()` method for printing text to the console.
 * 
 * In this program:
 * - `System.out` refers to the standard output stream (console).
 * - `println()` is a method that outputs the text followed by a new line.
 * 
 * The class name (j01HelloWorld) must match the filename (j01HelloWorld.java) for the program to compile and run correctly.
 */

public class j01HelloWorld {

    /**
     * Main method - the entry point of the program
     * 
     * This is where the program begins its execution.
     * 
     * @param args - Command line arguments (not used in this program)
     */
    public static void main(String args[]) {
        // Prints "Hello, World" to the console
        // System.out is the standard output stream (console).
        // println() method prints the text and adds a newline at the end.
        System.out.println("Hello, World");
    }

}
